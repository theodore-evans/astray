/// Draw a string into a u32 ARGB buffer at (px, py) with given color and scale.
pub fn draw_text(buf: &mut [u32], buf_w: usize, buf_h: usize, text: &str, px: usize, py: usize, color: u32, scale: usize) {
    let mut cx = px;
    for ch in text.chars() {
        let g = glyph(ch);
        for row in 0..GLYPH_H {
            for col in 0..GLYPH_W {
                if g[row] & (1 << (GLYPH_W - 1 - col)) != 0 {
                    for sy in 0..scale {
                        for sx in 0..scale {
                            let x = cx + col * scale + sx;
                            let y = py + row * scale + sy;
                            if x < buf_w && y < buf_h {
                                buf[y * buf_w + x] = color;
                            }
                        }
                    }
                }
            }
        }
        cx += (GLYPH_W + 1) * scale;
    }
}

/// Returns the pixel width of rendered text.
pub fn text_width(text: &str, scale: usize) -> usize {
    if text.is_empty() { return 0; }
    text.len() * (GLYPH_W + 1) * scale - scale
}
